#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	size_t			index;

	index = 0;
	while (s1[index] != '\0' && s2[index] != '\0' && s1[index] == s2[index])
		index++;
	return (s1[index] - s2[index]);
}

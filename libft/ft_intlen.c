#include "libft.h"

size_t	ft_intlen(int n)
{
	size_t	len;

	if (n == -2147483648)
		return (11);
	len = 1;
	if (n < 0)
	{
		n *= -1;
		len++;
	}
	while (n > 9)
	{
		n /= 10;
		len++;
	}
	return (len);
}
